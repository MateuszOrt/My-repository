class Solution(object):
    def isValidSudoku(self, board):
        for row in board:
            s=set()
            for i in row:
                if i != '.' and i not in s:
                    s.add(i)
                elif i == '.':
                    continue
                else:
                    return False    
            
        for j in range(len(board)):
            S=set()
            for row in board:
                 if row[j] != '.' and row[j] not in S:
                     S.add(row[j])
                 elif row[j] == '.':
                     continue    
                 else:
                     return False    
         
